                            <h2><center>Zakat Pertanian</center></h2>
                            <div class="down-content">
                                <p>Zakat hasil pertanian merupakan salah satu jenis Zakat Maal, objeknya meliputi hasil tumbuh-tumbuhan atau tanaman yang bernilai ekonomis seperti biji-bijian, umbi-umbian, sayur-mayur, buah-buahan dan lain-lain.</p>
                                <p>Diriwayatkan dari Ibnu umar RA, ia berkata: Nabi SAW bersabda: <i>“Terhadap tanaman yang disirami hujan dari langit dan dari mata air atau yang digenangi air selokan, dikeluarkan zakat sepersepuluhnya, sedangkan terhadap tanaman yang diairi dengan sarana pengairan seperduapuluhnya”</i> (HR. Bukhori dan Ahmad).</p>
                                <p>Firman Allah SWT yang mendukung untuk dikeluarkannya Zakat Pertaniaan tercantum dalam surat Al-An’am sebagai berikut : </p>
                                <p><i>“Makanlah dari buahnya (yang bermaca-macam itu) bila dia berbuah, dan tunaikanlah haknya dihari memetik hasilnya (dengan dikeluarkan zakatnya); dan janganlah kamu berlebih-lebihan. Sesungguhnya Allah tidak menyukai orang-orang yang berlebih-lebihan.”</i> (QS. Al – An’am: 141)</p>
                                <p>
                                    <b>Ketentuan : </b> <br>
                                    Mencapai nishab 653 kg gabah atau 520 kg jika yang dihasilkan adalah makanan pokok <br>
                                    <ol>
                                        <li>Jika selain makanan pokok, maka nishabnya disamakan dengan makanan pokok paling 2 2. umum di daerah</li>
                                        <li>Kadar zakat apabila diairi dengan air hujan, sungai, atau mata air, maka 10%</li>
                                        <li>Kadar zakat jika diairi dengan cara disiram (dengan menggunakan lat) atau irigasi maka zakatnya 5 %</li>
                                    </ol>
